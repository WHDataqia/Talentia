# Función auxiliar para obtener una competencia
def get_competencia(competencia_id):
    """Obtiene los datos de una competencia específica"""
    return COMPETENCIAS.get(competencia_id)

# Función auxiliar para obtener todos los descriptores de una competencia
def get_descriptores_competencia(competencia_id):
    """Obtiene todos los descriptores de una competencia"""
    competencia = COMPETENCIAS.get(competencia_id)
    if competencia:
        return competencia.get("descriptores", [])
    return []

# Función auxiliar para obtener un descriptor específico
def get_descriptor(competencia_id, descriptor_id):
    """Obtiene un descriptor específico de una competencia"""
    descriptores = get_descriptores_competencia(competencia_id)
    for desc in descriptores:
        if desc["id"] == descriptor_id:
            return desc
    return None
